        $(".IsNumber").keydown(function (e) {
            if ($.inArray(e.keyCode, [46, 8, 9, 27, 13, 110, 190]) !== -1 ||
                (e.keyCode == 65 && e.ctrlKey === true) || 
                (e.keyCode >= 35 && e.keyCode <= 39)) {
                     return;
            }
     
            if ((e.shiftKey || (e.keyCode < 48 || e.keyCode > 57)) && (e.keyCode < 96 || e.keyCode > 105)) {
                e.preventDefault();
            }
        });

        $(".IsAlpha").keydown(function (e) {
            if ((e.keyCode < 97 || e.keyCode > 122)//letras mayusculas
                && (e.keyCode < 65 || e.keyCode > 90) //letras minusculas
                && (e.keyCode != 45) //retroceso
                && (e.keyCode != 241) //ñ
                 && (e.keyCode != 209) //Ñ
                 && (e.keyCode != 32) //espacio
                 && (e.keyCode != 225) //á
                 && (e.keyCode != 233) //é
                 && (e.keyCode != 237) //í
                 && (e.keyCode != 243) //ó
                 && (e.keyCode != 250) //ú
                 && (e.keyCode != 193) //Á
                 && (e.keyCode != 201) //É
                 && (e.keyCode != 205) //Í
                 && (e.keyCode != 211) //Ó
                 && (e.keyCode != 218) //Ú
                ) {
                return;
            }

        });


    
    } );
    
